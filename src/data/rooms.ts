export const rooms: RoomDefinition[] = roomSeed.map((room, index) => {
  const y = index * ROOM_SPACING;
  const view = views[room.id] ?? DEFAULT_VIEW;
  return {
    ...room,
    y,
    camera: [view.camera[0], y + view.camera[1], view.camera[2]],
    target: [view.target[0], y + view.target[1], view.target[2]],
  };
});

export function getRoom(index: number): RoomDefinition {
  return rooms[index] ?? rooms[0]!;
}
